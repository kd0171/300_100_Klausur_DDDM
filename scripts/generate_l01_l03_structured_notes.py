def tex_escape(s: str) -> str:
    # Keep simple LaTeX math markers if user inserted them. Most content is plain Japanese.
    repl = {
        "\\": r"\textbackslash{}",
        "&": r"\&", "%": r"\%", "$": r"\$", "#": r"\#", "_": r"\_",
        "{": r"\{", "}": r"\}", "~": r"\textasciitilde{}", "^": r"\textasciicircum{}",
    }
    return "".join(repl.get(ch, ch) for ch in s)


def md_itemize(items: Sequence[Item], indent: int = 0) -> str:
    out = []
    pad = "  " * indent
    for it in items:
        if isinstance(it, tuple):
            out.append(f"{pad}- {it[0]}")
            out.append(md_itemize(it[1], indent + 1))
        else:
            out.append(f"{pad}- {it}")
    return "\n".join(out)


def tex_itemize(items: Sequence[Item], level: int = 0) -> str:
    out = [r"\begin{itemize}"]
    for it in items:
        if isinstance(it, tuple):
            out.append(r"\item " + tex_escape(it[0]))
            out.append(tex_itemize(it[1], level + 1))
        else:
            out.append(r"\item " + tex_escape(it))
    out.append(r"\end{itemize}")
    return "\n".join(out)


