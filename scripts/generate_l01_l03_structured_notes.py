def md_exam_section(exam_keys: Sequence[str]) -> str:
    if not exam_keys:
        return ""
    parts = ["### 過去問関連", ""]
    for key in exam_keys:
        for e in EXAM.get(key, []):
            parts += [f"#### {e['ref']}", f"- 問題内容: {e['content']}", f"- 期待される回答: {e['answer']}", ""]
    return "\n".join(parts)


def tex_exam_section(exam_keys: Sequence[str]) -> str:
    if not exam_keys:
        return ""
    out = [r"\NoteSection{過去問関連}"]
    for key in exam_keys:
        for e in EXAM.get(key, []):
            out.append(r"\ExamItem{" + tex_escape(e["ref"]) + "}{" + tex_escape(e["content"]) + "}{" + tex_escape(e["answer"]) + "}")
    return "\n".join(out)


def md_questions(qs: Sequence[Tuple[str, str]]) -> str:
    if not qs:
        return ""
    parts = ["### 確認問題と解答", ""]
    for i, (q, a) in enumerate(qs, 1):
        parts += [f"{i}. **{q}**", f"   - 解答・解説: {a}"]
    return "\n".join(parts)


def tex_questions(qs: Sequence[Tuple[str, str]]) -> str:
    if not qs:
        return ""
    out = [r"\NoteSection{確認問題と解答}", r"\begin{enumerate}"]
    for q, a in qs:
        out.append(r"\item \textbf{" + tex_escape(q) + r"}\\" + "\n" + tex_escape(a))
    out.append(r"\end{enumerate}")
    return "\n".join(out)


def md_concept(concepts: Sequence[str]) -> str:
    if not concepts:
        return ""
    return "### 解説\n\n" + "\n\n".join(concepts)


def tex_concept(concepts: Sequence[str]) -> str:
    if not concepts:
        return ""
    return r"\NoteSection{解説}" + "\n" + "\n\n".join(tex_escape(c) + r"\par" for c in concepts)


