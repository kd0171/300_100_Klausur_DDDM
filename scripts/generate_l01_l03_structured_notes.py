def compile_pdf(lec: str) -> None:
    cfg = LECTURES[lec]
    env = os.environ.copy()
    cmd = ["xelatex", "-interaction=nonstopmode", "-halt-on-error", cfg["texmain"]]
    subprocess.run(cmd, cwd=LATEX, env=env, check=True, stdout=subprocess.PIPE, stderr=subprocess.STDOUT)
    subprocess.run(cmd, cwd=LATEX, env=env, check=True, stdout=subprocess.PIPE, stderr=subprocess.STDOUT)
    pdf = LATEX / (Path(cfg["texmain"]).stem + ".pdf")
    OUTPUT.mkdir(exist_ok=True)
    out = OUTPUT / cfg["out"]
    if out.exists():
        out.unlink()
    pdf.replace(out)


