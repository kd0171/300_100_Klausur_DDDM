def write_section_tex(lec: str) -> None:
    cfg = LECTURES[lec]
    slides = SLIDES[lec]
    path_pdf = f"../Materials/2026/{cfg['pdf']}"
    out = [r"\section*{" + tex_escape(cfg["title"]) + "}", r"\addcontentsline{toc}{section}{" + tex_escape(cfg["title"]) + "}"]
    for p in range(1, cfg["pages"] + 1):
        s = slides[p]
        topic = s["topic"]
        block = get_block(topic)
        out += [
            r"\clearpage",
            r"\SlideHeader{" + tex_escape(cfg["title"]) + r"}{" + f"{p:03d}" + r"}{" + tex_escape(s["title"]) + r"}",
            r"\SlideImage{" + str(p) + r"}{" + path_pdf + r"}",
            r"\vspace{1.5mm}",
            r"\begin{multicols}{2}",
            r"\footnotesize",
            r"\raggedcolumns",
            r"\NoteSection{日本語訳}",
            tex_itemize(s["translation"]),
        ]
        conc = tex_concept(block.get("concept", []))
        if conc: out.append(conc)
        ep = tex_exam_points(topic, block.get("concept", []))
        if ep: out.append(ep)
        ex = tex_exam_section(block.get("exam_keys", []))
        if ex: out.append(ex)
        qs = tex_questions(block.get("questions", []))
        if qs: out.append(qs)
        out.append(r"\end{multicols}")
    SECTIONS.mkdir(parents=True, exist_ok=True)
    (SECTIONS / cfg["section"]).write_text("\n\n".join(out), encoding="utf-8")


